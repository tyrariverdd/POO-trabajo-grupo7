    private void botonCerrarRecojoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCerrarRecojoActionPerformed
        // TODO add your handling code here:
         if (pedidoActual == null) {
        areaResultado.setText("Primero debe crear un pedido.");

        } else if (!pedidoActual.getTipoEntrega().equals("Recojo en Tienda")) {
            areaResultado.setText("Solo puede cerrar recojo si el tipo de entrega es Recojo en Tienda.");

        } else {
            cajero.cerrarPedidoRecojo(pedidoActual.getNumeroCorrelativo());

            String texto = "Pedido cerrado como entregado.\n";
            texto = texto + "Pedido: " + pedidoActual.getNumeroCorrelativo() + "\n";
            texto = texto + "Estado: " + pedidoActual.getEstado() + "\n\n";

            if (pedidoActual.getTipoComprobanteSolicitado().equals("")) {
                texto = texto + pedidoActual.generarDetalleCuenta() + "\n\n";
                texto = texto + "No se solicito comprobante para este pedido.";

            } else {
                Comprobante comprobante = cajero.emitirComprobante(
                        pedidoActual.getNumeroCorrelativo(),
                        pedidoActual.getTipoComprobanteSolicitado(),
                        pedidoActual.getRucComprobante(),
                        pedidoActual.getRazonSocialComprobante()
                );

                if (comprobante == null) {
                    texto = texto + pedidoActual.generarDetalleCuenta() + "\n\n";
                    texto = texto + "No se pudo emitir el comprobante.";
                } else {
                    texto = texto + "COMPROBANTE EMITIDO\n\n";
                    texto = texto + comprobante.mostrarDatos();
                }
            }

            areaResultado.setText(texto);
        }
    }//GEN-LAST:event_botonCerrarRecojoActionPerformed

