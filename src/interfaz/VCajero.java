    private void botonRegistrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarClienteActionPerformed
        // TODO add your handling code here:
        String tipoDocumento = cajaTipoDocumento.getText();
        String documento = cajaDocumento.getText();
        String nombres = cajaNombres.getText();
        String apellidos = cajaApellidos.getText();
        String direccion = cajaDireccion.getText();
        String telefono = cajaTelefono.getText();

        if (tipoDocumento.equals("") || documento.equals("") || nombres.equals("") || apellidos.equals("")) {
            areaResultado.setText("Complete tipo de documento, documento, nombres y apellidos.");
        } else {
            Cliente cliente = gestionCajero.buscarClientePorDocumento(documento);

            if (cliente != null) {
                areaResultado.setText("El cliente ya esta registrado.");
            } else {
                cajero.registrarCliente(tipoDocumento, documento, nombres, apellidos, direccion, telefono);

                Cliente nuevoCliente = gestionCajero.buscarClientePorDocumento(documento);

                areaResultado.setText("Cliente registrado correctamente.\n\n"
                        + nuevoCliente.mostrarDatos());
            }
        }
    }//GEN-LAST:event_botonRegistrarClienteActionPerformed

    private void botonCrearPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCrearPedidoActionPerformed
        // TODO add your handling code here:
        String documentoCliente = cajaDocumentoPedido.getText();
        String tipoEntrega = "";

        if (checkDelivery.isSelected() && checkRecojo.isSelected()) {
            areaResultado.setText("Seleccione solo un tipo de entrega.");
        } else if (!checkDelivery.isSelected() && !checkRecojo.isSelected()) {
            areaResultado.setText("Seleccione un tipo de entrega.");
        } else if (documentoCliente.equals("")) {
            areaResultado.setText("Ingrese el documento del cliente.");
        } else {
            if (checkDelivery.isSelected()) {
                tipoEntrega = "Delivery";
            } else {
                tipoEntrega = "Recojo en Tienda";
            }

            Cliente cliente = gestionCajero.buscarClientePorDocumento(documentoCliente);

            if (cliente == null) {
                areaResultado.setText("No se puede crear el pedido porque el cliente no existe.");
            } else {
                pedidoActual = cajero.crearPedido(documentoCliente, tipoEntrega);

                textoPedidoActual.setText("Pedido actual: " + pedidoActual.getNumeroCorrelativo());

                cajaNumeroPedido.setText(pedidoActual.getNumeroCorrelativo() + "");
                cajaPedidoEntrega.setText(pedidoActual.getNumeroCorrelativo() + "");
                cajaPedidoComprobante.setText(pedidoActual.getNumeroCorrelativo() + "");

                cajaDniRepartidor.setText("");

                areaResultado.setText("Pedido creado correctamente.\n"
                        + "Numero de pedido: " + pedidoActual.getNumeroCorrelativo() + "\n"
                        + "Cliente: " + pedidoActual.getCliente().getNombreCompleto() + "\n"
                        + "Tipo entrega: " + pedidoActual.getTipoEntrega() + "\n"
                        + "Estado: " + pedidoActual.getEstado());
            }
        }
    }//GEN-LAST:event_botonCrearPedidoActionPerformed

    private void botonAgregarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarItemActionPerformed
        // TODO add your handling code here:
        if (pedidoActual == null) {
        areaResultado.setText("Primero debe crear un pedido.");
        } else {
            String codigo = cajaCodigoItem.getText();
            String cantidadTexto = cajaCantidad.getText();
            int cantidad = 0;

            if (cantidadTexto.equals("1")) {
                cantidad = 1;
            } else if (cantidadTexto.equals("2")) {
                cantidad = 2;
            } else if (cantidadTexto.equals("3")) {
                cantidad = 3;
            } else if (cantidadTexto.equals("4")) {
                cantidad = 4;
            } else if (cantidadTexto.equals("5")) {
                cantidad = 5;
            } else if (cantidadTexto.equals("6")) {
                cantidad = 6;
            } else if (cantidadTexto.equals("7")) {
                cantidad = 7;
            } else if (cantidadTexto.equals("8")) {
                cantidad = 8;
            } else if (cantidadTexto.equals("9")) {
                cantidad = 9;
            } else if (cantidadTexto.equals("10")) {
                cantidad = 10;
            }

            if (codigo.equals("")) {
                areaResultado.setText("Ingrese el codigo del item.");
            } else if (cantidad == 0) {
                areaResultado.setText("Ingrese una cantidad del 1 al 10.");
            } else {
                ItemMenu item = gestionAdmin.buscarItemMenu(codigo);

                if (item == null) {
                    areaResultado.setText("No existe producto o combo con ese codigo.");
                } else if (!item.hayStock(cantidad)) {
                    areaResultado.setText("No hay stock suficiente para ese item.");
                } else {
                    cajero.agregarItemAPedido(pedidoActual.getNumeroCorrelativo(), codigo, cantidad);

                    areaResultado.setText("Item agregado correctamente.\n"
                            + "Item: " + item.getNombre() + "\n"
                            + "Cantidad: " + cantidad + "\n\n"
                            + pedidoActual.generarDetalleCuenta());
                }
            }
        }
    }//GEN-LAST:event_botonAgregarItemActionPerformed

    private void botonAsignarRepartidorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAsignarRepartidorActionPerformed
        // TODO add your handling code here:
        if (pedidoActual == null) {
        areaResultado.setText("Primero debe crear un pedido.");

    } else if (!pedidoActual.getTipoEntrega().equals("Delivery")) {
        areaResultado.setText("Solo se asigna repartidor cuando el pedido es Delivery.");

    } else {
        String dni = cajaDniRepartidor.getText();
        Repartidor repartidor = null;

        for (int i = 0; i < gestionAdmin.getContadorEmpleados(); i++) {
            Empleado empleado = gestionAdmin.getListaEmpleados()[i];

            if ((empleado.getDni() + "").equals(dni) && empleado instanceof Repartidor) {
                repartidor = (Repartidor) empleado;
            }
        }

        if (dni.equals("")) {
            areaResultado.setText("Ingrese el DNI del repartidor.");

        } else if (repartidor == null) {
            areaResultado.setText("Repartidor no encontrado.");

        } else {
            cajero.asignarRepartidor(pedidoActual.getNumeroCorrelativo(), repartidor.getDni());

            areaResultado.setText("Repartidor asignado correctamente.\n"
                    + "Pedido: " + pedidoActual.getNumeroCorrelativo() + "\n"
                    + "Repartidor: " + repartidor.getNombreCompleto() + "\n"
                    + "Estado: " + pedidoActual.getEstado());
        }
    }
    }//GEN-LAST:event_botonAsignarRepartidorActionPerformed

    private void botonEmitirComprobanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEmitirComprobanteActionPerformed
        // TODO add your handling code here:
        if (pedidoActual == null) {
        areaResultado.setText("Primero debe crear un pedido.");

        } else if (!checkBoleta.isSelected() && !checkFactura.isSelected()) {
            areaResultado.setText("Seleccione Boleta o Factura.");

        } else {
            String tipoComprobante = "";
            String ruc = cajaRuc.getText();
            String razonSocial = cajaRazonSocial.getText();

            if (checkBoleta.isSelected()) {
                tipoComprobante = "Boleta";
            } else {
                tipoComprobante = "Factura";
            }

            if (tipoComprobante.equals("Factura") && ruc.equals("")) {
                areaResultado.setText("Para factura debe ingresar RUC.");

            } else if (tipoComprobante.equals("Factura") && razonSocial.equals("")) {
                areaResultado.setText("Para factura debe ingresar razon social.");

            } else {
                pedidoActual.solicitarComprobante(tipoComprobante, ruc, razonSocial);

                String texto = "Comprobante solicitado correctamente.\n";
                texto = texto + "Pedido: " + pedidoActual.getNumeroCorrelativo() + "\n";
                texto = texto + "Tipo: " + tipoComprobante + "\n";

                if (tipoComprobante.equals("Factura")) {
                    texto = texto + "RUC: " + ruc + "\n";
                    texto = texto + "Razon social: " + razonSocial + "\n";
                }

                texto = texto + "\n";

                if (pedidoActual.getTipoEntrega().equals("Recojo en Tienda")) {
                    texto = texto + "La " + tipoComprobante + " se emitira cuando cierre el recojo.";
                } else {
                    texto = texto + "La " + tipoComprobante + " se emitira cuando el repartidor registre la entrega.";
                }

                areaResultado.setText(texto);
            }
        }
    }//GEN-LAST:event_botonEmitirComprobanteActionPerformed

