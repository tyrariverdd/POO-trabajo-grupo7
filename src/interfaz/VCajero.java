    private void botonVerRepartidoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonVerRepartidoresActionPerformed
        // TODO add your handling code here:
        String texto = "REPARTIDORES DISPONIBLES\n\n";

        for (int i = 0; i < gestionAdmin.getContadorEmpleados(); i++) {
            Empleado empleado = gestionAdmin.getListaEmpleados()[i];

            if (empleado instanceof Repartidor) {
                Repartidor repartidor = (Repartidor) empleado;

                if (repartidor.isDisponible() && repartidor.isEstadoUnidad()) {
                    texto = texto + "DNI: " + repartidor.getDni() + "\n";
                    texto = texto + "Nombre: " + repartidor.getNombreCompleto() + "\n";
                    texto = texto + "Unidad: " + repartidor.getUnidadTransporte() + "\n";
                    texto = texto + "-----------------------------\n\n";
                }
            }
        }

        if (texto.equals("REPARTIDORES DISPONIBLES\n\n")) {
            texto = texto + "No hay repartidores disponibles.";
        }

        areaResultado.setText(texto);
    }//GEN-LAST:event_botonVerRepartidoresActionPerformed

