    private void botonRegistrarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarEmpleadoActionPerformed
        // TODO add your handling code here:
        String dniTexto = cajaDniEmpleado.getText();
        String nombres = cajaNombresEmpleado.getText();
        String apellidos = cajaApellidosEmpleado.getText();
        String password = cajaPasswordEmpleado.getText();
        String rol = cajaRolEmpleado.getText();

        if (dniTexto.equals("") || nombres.equals("") || apellidos.equals("") || password.equals("") || rol.equals("")) {
            AreaDeResultados.setText("Complete todos los datos del empleado.");
        } else if (!rol.equals("Administrador") && !rol.equals("Cajero") && !rol.equals("Repartidor")) {
            AreaDeResultados.setText("Rol incorrecto.\nUse: Administrador, Cajero o Repartidor.");
        } else {
            int dni = 0;

            for (int i = 0; i < dniTexto.length(); i++) {
                dni = dni * 10 + (dniTexto.charAt(i) - '0');
            }

            Empleado empleado = gestionAdmin.buscarEmpleadoPorDni(dni);

            if (empleado != null) {
                AreaDeResultados.setText("Ya existe un empleado con ese DNI.");
            } else {
                gestionAdmin.registrarEmpleado(dni, nombres, apellidos, password, rol, gestionCajero);

                AreaDeResultados.setText("Empleado registrado correctamente.\n"
                        + "DNI: " + dni + "\n"
                        + "Nombre: " + nombres + " " + apellidos + "\n"
                        + "Rol: " + rol);
            }
        }
    }//GEN-LAST:event_botonRegistrarEmpleadoActionPerformed

