    private void botonModificarComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarComboActionPerformed
        // TODO add your handling code here:
        String codigo = cajaCodigoCombo.getText();
        String nombre = cajaNombreCombo.getText();
        String precioTexto = cajaPrecioCombo.getText();

        if (codigo.equals("") || nombre.equals("") || precioTexto.equals("")) {
            AreaDeResultados.setText("Complete todos los datos del combo.");
        } else {
            Combo combo = gestionAdmin.buscarComboPorCodigo(codigo);

            if (combo == null) {
                AreaDeResultados.setText("Combo no encontrado.");
            } else {
                int precio = 0;

                for (int i = 0; i < precioTexto.length(); i++) {
                    precio = precio * 10 + (precioTexto.charAt(i) - '0');
                }

                gestionAdmin.modificarCombo(codigo, nombre, precio);

                AreaDeResultados.setText("Combo modificado correctamente.");
            }
        }
    }//GEN-LAST:event_botonModificarComboActionPerformed

