    private void botonAgregarProductoComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarProductoComboActionPerformed
        // TODO add your handling code here:
        String codigoCombo = cajaComboAgregar.getText();
        String codigoProducto = cajaProductoAgregar.getText();

        if (codigoCombo.equals("") || codigoProducto.equals("")) {
            AreaDeResultados.setText("Ingrese codigo del combo y codigo del producto.");
        } else {
            Combo combo = gestionAdmin.buscarComboPorCodigo(codigoCombo);
            Producto producto = gestionAdmin.buscarProductoPorCodigo(codigoProducto);

            if (combo == null) {
                AreaDeResultados.setText("Combo no encontrado.");
            } else if (producto == null) {
                AreaDeResultados.setText("Producto no encontrado.");
            } else {
                gestionAdmin.agregarProductoACombo(codigoCombo, codigoProducto);
                AreaDeResultados.setText("Producto agregado al combo correctamente.");
            }
        }
    }//GEN-LAST:event_botonAgregarProductoComboActionPerformed

